			<h1>What is City Fare?</h1>
			<p>City Fare Meals on Wheels, a program of Saint Anthony’s Community Center, has operated in Wilmington since 1986. We provide hot, nourishing meals to homebound elderly and disabled persons in New Castle County who are unable to prepare meals for themselves and who have no one to do so for them. We also provide professional case management to every client. The meals, daily contact by caring volunteers, and staff who coordinate other services as needed allow frail, otherwise forgotten, homebound persons to remain living independently in their own homes.<p>
			
			<p>Volunteers and financial contributions have always been vital in fulfilling our mission . Without this support, it would be impossible to feed the people we do each day. Volunteers not only deliver meals but also, perhaps even more importantly, show concern, love, and compassion. For many clients, the Meals on Wheels volunteers are the only people they see each day.</p> 
			
			<p>In addition to delivering Meals on Wheels, City Fare serves meals at lunchtime in senior center dining rooms and senior housing complexes throughout New Castle County.</p>
			
			<p>City Fare receives funding from the State of Delaware through the Division of Services for Aging and Adults with Physical Disabilities, donations from the people it serves, and significant contributions from Meals on Wheels Delaware and the Kenny Family Foundation.</p>
			
